    let gimli = 5;
    let sauron = 1;

    let result1 = elvenAddition(frodo, sam);
    let result2 = fairyMultiplication(gandalf, aragorn);
    let result3 = elvenDivision(result2, legolas);
    let result4 = dwarvenSubtraction(result3, gimli);
    let result5 = entPower(result4, sauron);
    let result6 = hobbitRemainder(result5, frodo);

    // A variable to hold the final magical result
    let finalResult = result6;

    // Display the final result with a touch of elven grace
    console.log("The final magical result is: " + finalResult);
